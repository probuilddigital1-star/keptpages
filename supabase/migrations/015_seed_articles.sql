Sometimes a word is just gone. The ink has faded too much, the pen skipped, or the card got wet. Here is what to do:

1. **Mark it.** Write "[unclear]" in your transcription where the word should be. Do not guess and fill in something that might be wrong.
2. **Look at other recipes by the same person.** If your grandmother wrote 40 recipe cards, she probably used the same words over and over. Find "teaspoon" written clearly on another card, and now you know what her "teaspoon" looks like on the hard one.
3. **Try the recipe.** Sometimes cooking the dish tells you what the missing word probably was. If the recipe calls for "[unclear] sauce" and it is clearly an Italian dish, it was probably "tomato sauce."

