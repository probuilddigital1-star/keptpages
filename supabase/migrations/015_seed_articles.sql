### The Most Commonly Confused Letters

Some cursive letters look almost identical to each other. Here are the pairs that trip people up the most:

- **Lowercase "e" and "i":**In many older styles, both are just tiny bumps. Look at the height. An "e" is usually a bit wider and rounder.
- **Lowercase "n" and "u":**These are literally the same shape flipped upside down. Context is your best friend here. "Run" makes sense. "Rnn" does not.
- **Lowercase "a" and "o":**Some writers close their "a" so tightly it looks like an "o." If you see a word that looks like "ond," it probably says "and."
- **Capital "L" and "S":**In old Palmer method cursive, these can look nearly identical. A capital "S" usually has a bigger top loop.
- **Capital "I" and "J":**Many older writers made these the same way. Look at the word that follows and use context.
- **Lowercase "f" and "p":**Both drop below the line. The "f" has a crossbar or loop in the middle. The "p" does not.

